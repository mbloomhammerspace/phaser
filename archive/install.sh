        log "INFO" "Running AI-powered cluster diagnostics..."
        run_cluster_diagnostics
    else
        ansible-playbook -i "$INVENTORY_FILE" playbooks/04-validate.yml -v
    fi
    
    log "INFO" "Installation completed successfully!"
    show_access_info
}

